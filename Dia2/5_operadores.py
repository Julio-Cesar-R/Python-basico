y = 3
x = 7
z = 2
print(
    f"{x} dividido al piso de {z} es igual a {x//z}"
)  # desaparece los puntos decimales
print(f"{x} modulo o residuo de {y} es igual a {x%y}")  # muestra residuo de la division
print(f"{x} elevado a {y} es igual a {x**y}")  # elevar a un exponente
print(
    f"La raiz cuadrada de {x} es igual a {x**0.5}"
)  # raiz cuadrada 0.5 siempre sera el parametro

