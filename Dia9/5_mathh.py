print(numero)

numero1 = math.ceil(17.6)  # redondea hacia arriba
print(numero1)

numero2 = math.pi  # valor de pi
print(numero2)

numero3 = math.log(100, 5)  # logaritmos(a que numero debo elevar a 5 para que sea 100
print(numero3)
print(5**numero3)

numero4 = math.tan(3333)  # tangente
print(numero4)

numero5 = math.cos(3333)  # coseno
print(numero5)
